namespace SR\Doctrine\ORM\Model\Identity;

/**
 * Class IdMutableTrait
 */
trait IdMutableTrait
{
    use IdTrait;

    /**
     * @param int $id
     *
     * @return $this
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return $this
     */
    public function clearId()
    {
        $this->id = null;
        
        return $this;
    }
}

